// ------------------------------------------------------------------
// I2C + SSD1306 low-level
// ------------------------------------------------------------------
static void ssd1306_write_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {0x00, cmd};
    i2c_master_transmit(s_dev, buf, 2, pdMS_TO_TICKS(50));
}

static void ssd1306_write_data(const uint8_t *data, size_t len)
{
    // I2C buffer for header + data; allocate on heap for larger transfers
    uint8_t *buf = malloc(len + 1);
    if (buf == NULL) return;
    buf[0] = 0x40;
    memcpy(buf + 1, data, len);
    i2c_master_transmit(s_dev, buf, len + 1, pdMS_TO_TICKS(100));
    free(buf);
}

// ------------------------------------------------------------------
// Public API
// ------------------------------------------------------------------
void display_init(void)
{
    s_mutex = xSemaphoreCreateMutex();

    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = I2C_NUM_1,
        .sda_io_num = I2C_SDA_GPIO,
        .scl_io_num = I2C_SCL_GPIO,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags = { .enable_internal_pullup = true },
    };

    i2c_master_bus_handle_t bus;
    ESP_ERROR_CHECK(i2c_new_master_bus(&bus_cfg, &bus));

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = SSD1306_ADDR,
        .scl_speed_hz = 400000,
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus, &dev_cfg, &s_dev));

    // SSD1306 init sequence
    ssd1306_write_cmd(0xAE); // display off
    ssd1306_write_cmd(0xD5); // set osc clock
    ssd1306_write_cmd(0x80);
    ssd1306_write_cmd(0xA8); // set multiplex
    ssd1306_write_cmd(0x3F); // 64
    ssd1306_write_cmd(0xD3); // set display offset
    ssd1306_write_cmd(0x00);
    ssd1306_write_cmd(0x40); // start line
    ssd1306_write_cmd(0x8D); // charge pump
    ssd1306_write_cmd(0x14); // enable
    ssd1306_write_cmd(0x20); // memory addressing mode
    ssd1306_write_cmd(0x02); // page addressing mode
    ssd1306_write_cmd(0x21); // set column address range
    ssd1306_write_cmd(0x00); // start = 0
    ssd1306_write_cmd(0x7F); // end = 127
    ssd1306_write_cmd(0x22); // set page address range
    ssd1306_write_cmd(0x00); // start = 0
    ssd1306_write_cmd(0x07); // end = 7
    ssd1306_write_cmd(0xA1); // segment remap
    ssd1306_write_cmd(0xC8); // COM scan direction
    ssd1306_write_cmd(0xDA); // COM pins
    ssd1306_write_cmd(0x12);
    ssd1306_write_cmd(0x81); // contrast
    ssd1306_write_cmd(0xCF);
    ssd1306_write_cmd(0xD9); // pre-charge
    ssd1306_write_cmd(0xF1);
    ssd1306_write_cmd(0xDB); // VCOMH deselect
    ssd1306_write_cmd(0x40);
    ssd1306_write_cmd(0xA4); // resume to RAM content
    ssd1306_write_cmd(0xA6); // normal display
    ssd1306_write_cmd(0xAF); // display on

    display_clear();
    ESP_LOGI(TAG, "SSD1306 initialized");
}

void display_clear(void)
{
    xSemaphoreTake(s_mutex, portMAX_DELAY);

    uint8_t zero[128] = {0};

    for (int page = 0; page < SCREEN_PAGES; page++) {
        ssd1306_write_cmd(0xB0 + page);
        ssd1306_write_cmd(0x00);  // column low
        ssd1306_write_cmd(0x10);  // column high
        for (int col = 0; col < SCREEN_WIDTH; col += 128) {
            ssd1306_write_data(zero, 128);
        }
    }

    xSemaphoreGive(s_mutex);
}

void display_set_line(int line, const char *text)
{
    if (line < 0 || line >= SCREEN_PAGES) return;

    xSemaphoreTake(s_mutex, portMAX_DELAY);

    uint8_t row[128] = {0};
    size_t len = strlen(text);

    for (size_t i = 0; i < len && i < 16; i++) {
        uint8_t c = (uint8_t)text[i];
        if (c < 32 || c > 127) c = 32;
        memcpy(&row[i * 8], font8x8[c - 32], 8);
    }

    ssd1306_write_cmd(0xB0 + line);
    ssd1306_write_cmd(0x00);
    ssd1306_write_cmd(0x10);
    ssd1306_write_data(row, 128);

    xSemaphoreGive(s_mutex);
}

void display_set_volume(int vol)
{
    char buf[17];
    // Volume number
    snprintf(buf, sizeof(buf), "Vol: %3d%%", vol);
    display_set_line(0, buf);

    // Volume bar on line 1 (4-pixel-tall horizontal bar)
    uint8_t row[128] = {0};
    int bar_len = vol * 128 / 100;
    for (int i = 0; i < bar_len && i < 128; i++) {
        row[i] = 0x3C;  // 00111100 = 4 middle pixels of the 8-pixel page
    }

    xSemaphoreTake(s_mutex, portMAX_DELAY);
    ssd1306_write_cmd(0xB0 + 1);  // page 1
    ssd1306_write_cmd(0x00);
    ssd1306_write_cmd(0x10);
    ssd1306_write_data(row, 128);
    xSemaphoreGive(s_mutex);
}

void display_set_status(const char *status)
{
    display_set_line(3, status);
}

void display_refresh(void)
{
    // No-op: updates happen immediately in set_line/set_volume
}
